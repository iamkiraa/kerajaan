      <div class="container">

        <header class="section-header">
          <h3>Sejarah</h3>
          <p>Terengganu menyimpan pelbagai sejarah yang masih belum diketahui, dan ramai yang tidak mengetahui bahawa sejarah ini bermula sejak beribu-ribu tahun dahulu. Penemuan fosil di Gua Bewah, Tasik Kenyir telah membuktikan penempatan manusia di Zaman Batu iaitu seawal 8,000 hingga 20,000 tahun dahulu. Terengganu juga pernah disebut dalam sejarah lama Cina, dan dipanggil “Teng-ngan-yu” pada abad ke-12, di mana ia merupakan sebuah pelabuhan utama untuk melakukan perdagangan. Ia menjadi sebahagian daripada Empayar Sri Wijaya semasa abad ke-2. Ptolemy, ahli astronomi Greek yang hidup semasa zaman pemerintahan Sri Wijaya, turut menyebut tentang dua pelabuhan di Semenanjung Tanah Melayu iaitu Primula dan Kole, di pantai timur Terengganu.<br>
<br>
			Batu Bersurat Terengganu adalah satu penemuan penting yang telah membuktikan kedatangan awal Islam pada abad ke-14 di negara ini, bahkan mendahului Zaman Kesultanan Melayu Melaka. Seterusnya pada abad ke-18, Kesultanan Terengganu pula ditubuhkan oleh Tun Zainal Abidin I.<br>
<br>
			Sifat rakyat Terengganu yang rajin dan tekun telah menjadikan Terengganu masyhur dan terkenal pada abad ke-19. Hugh Clifford, Residen British di Pahang dalam catatannya pernah menyatakan ketakjubannya terhadap negeri Terengganu. Beliau mendapati Terengganu adalah sebuah negeri yang kaya dengan kraftangan tradisional dan pelbagai industri, dari pembinaan bot, ukiran kayu, tembaga, songket dan batik, sehinggalah kepada pembinaan rumah tanpa menggunakan paku. Sungai Terengganu turut menjadi pusat perdagangan yang sangat sibuk, dan pernah mendapat jolokan “Birmingham of Peninsula”, yang melambangkan kepelbagaian industri yang terdapat di Terengganu. Bahkan, sejarawan turut menganggap Terengganu sebagai pusat bermulanya ketamadunan Melayu.</p>
